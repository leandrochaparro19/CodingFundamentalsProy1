class Pokemon{
    constructor(nombre,vida,ataque,defensa,nivel){
    this.nombre=nombre;
    this.vida=vida;
    this.ataque=ataque;
    this.defensa=defensa;
    this.nivel=nivel;
    }
    mostrarInfo(){
        return `
        Pokemon:${this.nombre}
        Vida:${this.vida}
        Ataque:${this.ataque}
        Defensa:${this.defensa}
        Nivel:${this.nivel}`;
    }
    set SetDaño(valor){
        if(this.defensa.length>0){
        if(this.defensa.length>valor.length){
        this.defensa = this.defensa.substring(0,this.defensa.length-valor.length);
        }else{
        this.defensa = this.defensa.substring(0,this.defensa.length-valor.length);
        this.vida =this.vida.substring(0,this.vida.length-(valor.length-this.defensa.length));
        }
        }
        else{
        this.vida =this.vida.substring(0,this.vida.length-valor.length);
        }
    }
    set SetEvloucion(aux){
        this.vida=this.vida.concat(aux);
        this.defensa=this.defensa.concat(aux);
        this.ataque=this.ataque.concat("█████");
        this.nivel++;
    }
    get GetVida(){
        return this.vida;
    }
    Muere(){
        if(this.vida.length<=0){
            return true;
        }else{
            return false;
        }
    }
}

